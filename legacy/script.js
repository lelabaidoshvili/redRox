document.addEventListener('DOMContentLoaded', () => {
    const header = document.querySelector('#main-header');
    const reveals = document.querySelectorAll('.reveal-text, .fade-in, .glass-card, .project-item');
    const langBtns = document.querySelectorAll('.lang-btn');

    // Language logic
    const setLanguage = (lang) => {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang][key]) {
                el.innerHTML = translations[lang][key];
            }
        });

        // Toggle language class on body for styling
        if (lang === 'ge') {
            document.body.classList.add('lang-ge');
        } else {
            document.body.classList.remove('lang-ge');
        }

        // Update active button state
        langBtns.forEach(btn => {
            btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
        });

        // Save preference
        localStorage.setItem('redRox_lang', lang);
    };

    langBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.getAttribute('data-lang');
            setLanguage(lang);
        });
    });

    // Load saved preference
    const savedLang = localStorage.getItem('redRox_lang') || 'en';
    setLanguage(savedLang);

    // Header scroll effect
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            header.classList.add('scrolled');
        } else {
            header.classList.remove('scrolled');
        }
    });

    // Intersection Observer for animations
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('active');
            }
        });
    }, observerOptions);

    reveals.forEach(el => {
        observer.observe(el);
    });

    // Mobile menu toggle
    const mobileMenu = document.getElementById('mobile-menu');
    if (mobileMenu) {
        mobileMenu.addEventListener('click', () => {
            document.querySelector('.nav-links').classList.toggle('mobile-active');
            mobileMenu.classList.toggle('open');
        });
    }

    // Prism Nexus Mouse Tracking
    const nexus = document.querySelector('.prism-nexus');
    if (nexus) {
        window.addEventListener('mousemove', (e) => {
            const x = (e.clientX / window.innerWidth - 0.5) * 20;
            const y = (e.clientY / window.innerHeight - 0.5) * 20;
            nexus.style.transform = `rotateX(${y}deg) rotateY(${x}deg)`;
        });
    }
});
